export function traduzir(idioma: Idioma, chave: string, vars?: Record<string, string>): string {
  const dict = I18N[idioma] || I18N.pt;
  let texto = dict[chave] ?? I18N.pt[chave] ?? chave;
  if (vars) Object.entries(vars).forEach(([k, val]) => { texto = texto.replace(`{${k}}`, val); });
  return texto;
}
